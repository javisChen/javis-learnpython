L = ["javis", "ben", "mary", "wade", "kobe"]

# 取前三个元素
print("取前三个元素:", L[0:3])

# 从索引1开始,取2个元素出来
print("从索引1开始,取2个元素出来:", L[1:3])

L = list(range(100))

# 倒数切片,倒数第一个索引是-1,取后10个数
print("倒数切片,取后10个数:", L[-10:])

# 前10个数,每两个取一个
print("倒数切片,取后10个数:", L[:10:2])

# 所有数,取能被5整除的数
print("所有数,取能被5整除的数:", L[::5])

# 字符串也可以看成一种list,每个元素就是一个字符
print("字符串切片:", "ABCDEF"[0:1])


